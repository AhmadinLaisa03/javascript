let a = true;
let b = false;
let c = true;
let d = false;

// OR
// bernilai true jika salah satu kondisi bernilai true
console.log(b || d || a);

// AND
// bernilai true jika semua kondisi bernilai true,  dan bernilai false jika  terdapat satu saja nilai false
console.log(a && c);

// operator pembanding
let x = 8; let y = 7; let z = 9;

console.log(x > y);
console.log(x < y);
console.log(x == x);
console.log(x <= z);
console.log(z >= x);

let limo = "5";
let lima = 5;

// sama dengan dua kali berarti nilai nya sama
// sama dengan tiga kali berarti identik, tidak hanya sama dari segi isi nya namun juga dari tipe data nya
